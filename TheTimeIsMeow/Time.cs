        public void DisplayTime(List<int> numbers) {
            List<string> numberStrings = new List<string>();

            foreach (int number in numbers) {
                numberStrings.Add(Map[number]);
            }

            FormatNumbers(numberStrings);
        }

        private void FormatNumbers(List<string> numbers) {
            List<string> firstLine = new List<string>();
            List<string> secondLine = new List<string>();
            List<string> thirdLine = new List<string>();
            List<string> fourthLine = new List<string>();
            List<string> fifthLine = new List<string>();
            List<string> sixthLine = new List<string>();
            List<List<string>> lines = new List<List<string>>();

            lines.Add(firstLine);
            lines.Add(secondLine);
            lines.Add(thirdLine);
            lines.Add(fourthLine);
            lines.Add(fifthLine);
            lines.Add(sixthLine);

            foreach (string number in numbers) {
                string[] line = number.Split(',');

                for (int i = 0; i < line.Length; i++) {
                    lines[i].Add(line[i]);
                }
            }

            foreach (List<string> line in lines) {
                Console.WriteLine(String.Join(" ", line));
            }
        }
